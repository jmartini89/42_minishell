#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	t_byte	*ptr;

	ptr = b;
	while (len)
	{
		*ptr = (t_byte)c;
		ptr++;
		len--;
	}
	return (b);
}
